    CANTSUMINISTRO INT(10),
    EXISTENCIAS INT(10)
);

CREATE TABLE INCLUYE
(
    NUMPEDIDO INT(10),
    NUMARTICULO INT(10),
    CANTIDAD INT(10)
);

CREATE TABLE PEDIDO
(
    NUMPEDIDO INT(10),

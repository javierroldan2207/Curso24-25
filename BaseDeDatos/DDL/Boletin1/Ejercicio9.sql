CREATE TABLE CLIENTE
(
    NUMCLIENTE INT(10) AUTO_INCREMENT,
    SALDO INT(10),
    DESCUENTO INT(10),
    CONSTRAINT PK_CLIENTE PRIMARY KEY (NUMCLIENTE)
);

CREATE TABLE FABRICA
(
    NUMFABRICA INT(10) AUTO_INCREMENT,
    TELEFONO INT(10),
    CONSTRAINT PK_FABRICA PRIMARY KEY (NUMFABRICA)
);

CREATE TABLE ARTICULO
(
    NUMARTICULO INT(10) AUTO_INCREMENT,
    DESCRIPCION VARCHAR(25),
    CONSTRAINT PK_ARTICULO PRIMARY KEY (NUMARTICULO)
);

CREATE TABLE DIRECCION
(
    CODDIRECCION VARCHAR(15),
    VIA VARCHAR(10),
    NOMBREVIA VARCHAR(15),
    NUMERO INT(10),
    PISO INT(10),
    PORTAL VARCHAR(15),
    CODPOSTAL INT(10)
);

CREATE TABLE DISTRIBUYE
(
    NUMARTICULO INT(10),

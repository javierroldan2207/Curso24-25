/*CREACIÓN DE TABLAS*/

CREATE TABLE EQUIPO
(
NOMBRE_EQUIPO VARCHAR2(25),
CONSTRAINT PK_EQUIPO PRIMARY KEY (NOMBRE_EQUIPO)
);

CREATE TABLE CICLISTA
(
NOMBRE_CICLISTA VARCHAR2(25),
F_NACIMIENTO DATE,
NACIONALIDAD VARCHAR2(25),
CONSTRAINT PK_CICLISTA PRIMARY KEY (NOMBRE_CICLISTA)
);

CREATE TABLE CICLISTA_EQUIPO
(
NOMBRE_EQUIPO VARCHAR2(25),
NOMBRE_CICLISTA VARCHAR2(25),
CONSTRAINT PK_CICLISTA_EQUIPO PRIMARY KEY (NOMBRE_CICLISTA,NOMBRE_EQUIPO),
CONSTRAINT FK1_EQUIPO_CICLISTA FOREIGN KEY (NOMBRE_CICLISTA) REFERENCES CICLISTA(NOMBRE_CICLISTA),
CONSTRAINT FK2_EQUIPO_CICLISTA FOREIGN KEY (NOMBRE_EQUIPO) REFERENCES EQUIPO(NOMBRE_EQUIPO)
);

CREATE TABLE PRUEBA 
(
NOMBRE_PRUEBA VARCHAR2(25),
KM_TOTALES NUMBER(10),
ANYO DATE,
GANADOR VARCHAR2(25),
CONSTRAINT PK_PRUEBA PRIMARY KEY (NOMBRE_PRUEBA),
CONSTRAINT FK1_PRUEBA FOREIGN KEY (GANADOR) REFERENCES CICLISTA(NOMBRE_CICLISTA)
);




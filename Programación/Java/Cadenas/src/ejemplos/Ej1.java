public class Ej1 {

	public static void main(String[] args) {
		
		char car_c = 'c';
		System.out.println(Character.isUpperCase(car_c));
		System.out.println(Character.isLowerCase(car_c));
		System.out.println(Character.isDigit(car_c));
		System.out.println(Character.isLetter(car_c));
	}

}
